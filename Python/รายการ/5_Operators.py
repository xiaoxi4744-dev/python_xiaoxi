# ** ยกกำลัง
# // หารไม่เอาเศษไม่เอาทศนิยม
num1 = 10
num2 = 7
print("ตัวอย่างการใช้ตัวดำเนินการทางคณิตศาสตร์")
print("num1 =",num1)
print("num2 =",num2)
print("----------ผลลัพธ์----------")
print("num1 + num2 =",num1 + num2)   # บวก
print("num1 - num2 =",num1 - num2)   # ลบ
print("num1 * num2 =",num1 * num2)   # คูณ
print("num1 / num2 =",num1 / num2)   # หาร
print("num1 ** num2 =",num1 ** num2) # ยกกำลัง
print("num1 // num2 =",num1 // num2) # หารไม่เอาเศษ
print("num1 % num2 =",num1 % num2)   # หารเอาเศษ

# ตัวอย่างการใช้ตัวดำเนินการเปรียบเทียบ
print("ตัวอย่างการใช้ตัวดำเนินการเปรียบเทียบ")
a = 10
b = 7
print("a =",a)
print("b =",b)
print("----------ผลลัพธ์----------")
print("a == b :",a == b)   # เท่ากับ
print("a != b :",a != b)   # ไม่เท่ากับ
print("a > b  :",a > b)    # มากกว่า
print("a < b  :",a < b)    # น้อยกว่า
print("a >= b :",a >= b)   # มากกว่าหรือเท่
print("a <= b :",a <= b)   # น้อยกว่าหรือเท่

# ตัวอย่างการใช้ตัวดำเนินการทางตรรกะ
print("ตัวอย่างการใช้ตัวดำเนินการทางตรรกะ")
x = True
y = False
print("x =",x)
print("y =",y)
print("----------ผลลัพธ์----------")
print("x and y :",x and y) # และ
print("x or y  :",x or y)  # หรือ
print("not x   :",not x)    # ไม่
print("not y   :",not y)    # ไม่

