name = "xiaoxi"
age = 18
gpa = 3.5
status = True

#แสดงข้อมูลตัวแปร
print("ชื่อนักเรียน : ",name)  #แสดงค่าของตัวแปร name
print("อายุนักเรียน : ",age)   #แสดงค่าของตัวแปร age
print("เกรดเฉลี่ย : ",gpa)   #แสดงค่าของตัวแปร gpa
print("สถานภาพ : ",status) #แสดงค่าของตัวแปร status
print("-----")
print(type(name))#แสดงชนิดข้อมูลของตัวแปร name
print(type(age))#แสดงชนิดข้อมูลของตัวแปร age
print(type(gpa))#แสดงชนิดข้อมูลของตัวแปร gpa
print(type(status))#แสดงชนิดข้อมูลของตัวแปร status